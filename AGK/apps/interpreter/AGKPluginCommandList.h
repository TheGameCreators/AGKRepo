AGKVoidFunc GetAGKFunction( const char* func )
{
	if ( !func )
	{
		agk::SetErrorMode( 2 );
		agk::Error( "Plugin tried to request AGK function with a null pointer" );
	}
	int high = g_iNumAGKFunctions-1;
	int low = 0;
	while( high >= low )
	{
		int mid = (high+low)/2;
		int result = strcmp( g_ppAGKFunctionList[mid].szName, func );
		if( result > 0 ) high = mid-1;
		else if ( result < 0 ) low = mid+1;
		else return g_ppAGKFunctionList[mid].pFunc;
	}
	if ( strcmp( func, "GETSOCKETLISTNERCONNECTION_L_L" ) == 0 ) return (AGKVoidFunc) agk::GetSocketListenerConnection;
	if ( strcmp( func, "SET3DPHYSICSRADOLLBONESVISIBLE_0_L_L" ) == 0 ) return (AGKVoidFunc)  (void(*)(UINT,int)) (agk::Set3DPhysicsRagdollBonesVisible);
	if ( strcmp( func, "SET3DPHYSICSHINGEJOINTMAXMOTORIMPLUSE_0_L_F" ) == 0 ) return (AGKVoidFunc) (void(*)(UINT,float)) (agk::Set3DPhysicsHingeJointMaxMotorImpulse);
	if ( strcmp( func, "SET3DPHYSICSTWISTJOINTMAXMOTORIMPLUSE_0_L_F" ) == 0 ) return (AGKVoidFunc) (void(*)(UINT,float)) (agk::Set3DPhysicsTwistJointMaxMotorImpulse);
	
	agk::SetErrorMode( 2 );
	uString err;
	err.Format( "Plugin tried to request AGK function that doesn't exist: %s", func );
	agk::Error( err );
	return 0;
}

